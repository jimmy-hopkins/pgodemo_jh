CREATE TABLE public.employees (
	employee_id int4 NOT NULL DEFAULT nextval('employees_seq'::regclass),
	first_name varchar(20) NULL,
	last_name varchar(25) NOT NULL,
	email varchar(25) NOT NULL,
	phone_number varchar(20) NULL,
	hire_date timestamp NOT NULL,
	job_id varchar(10) NOT NULL,
	salary float8 NULL,
	commission_pct float4 NULL,
	manager_id int4 NULL,
	department_id int2 NULL,
	CONSTRAINT emp_salary_min CHECK ((salary > (0)::double precision)),
	CONSTRAINT employees_pkey PRIMARY KEY (employee_id)
);
CREATE INDEX emp_department_ix ON public.employees USING btree (department_id);
CREATE UNIQUE INDEX emp_email_uk ON public.employees USING btree (email);
CREATE INDEX emp_job_ix ON public.employees USING btree (job_id);
CREATE INDEX emp_manager_ix ON public.employees USING btree (manager_id);
CREATE INDEX emp_name_ix ON public.employees USING btree (last_name, first_name);

